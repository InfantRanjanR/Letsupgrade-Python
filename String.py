str = "infant"#declaring string
print(str)
#output infant

print(str.capitalize())#Converts the first character to upper case
#output Infant

print(str.isdecimal())#Returns True if all characters in the string are decimals
#output False

print(str.encode())#Returns an encoded version of the string
#output b'infant'

print(str.endswith("t"))#Returns true if the string ends with the specified value
#output True

print(str.isalpha())#Returns True if all characters in the string are in the alphabet
#output True